package oopsConceptPart1;

public class SuperKeyConceptClassA {
	
	public SuperKeyConceptClassA() {
		System.out.println("Parent calss Default Const");
	}
	
public SuperKeyConceptClassA(int i) {
	System.out.println("Parent calss Single Param Const i :" +i);
	}

public SuperKeyConceptClassA(int i, int j) {
	System.out.println("Parent calss Multiple Param Const i :"+i);
	System.out.println("Parent calss Multiple Param Const j :"+j);
}

}
